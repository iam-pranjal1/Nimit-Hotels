<script>
    function showAlert(type, msg) {
        var bsClass = (type === "success") ? "alert-success" : "alert-danger";

        var element = document.createElement("div");
        element.innerHTML = `
            <div class="alert ${bsClass} alert-dismissible fade custom-alert show" role="alert">
                <strong class="me-3">${msg}</strong>
                <button type="button" class="close" data-dismiss="alert" aria-label="Close">
                    <span aria-hidden="true">&times;</span>
                </button>
            </div>
        `;

    
        document.body.appendChild(element);
    }


    function setActive() {
        let navbar = document.getElementById("adminDropdown");
        let a_tags = navbar.getElementsByTagName('a');

        for (let i = 0; i < a_tags.length; i++) {
            let file = a_tags[i].href.split('/').pop();
            let file_name = file.split('.')[0];

            if (document.location.href.indexOf(file_name) >= 0) {
                a_tags[i].classList.add('active');
            }
        }
    }

    setActive(); // Ensure to call the function after defining it.
</script>